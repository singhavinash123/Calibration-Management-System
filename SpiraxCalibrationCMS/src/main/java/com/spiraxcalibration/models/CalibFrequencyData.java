package com.spiraxcalibration.models;

public class CalibFrequencyData {
		private String calibType;
		private Integer calibFrequency;
		/**
		 * @return the calibType
		 */
		public String getCalibType() {
			return calibType;
		}
		/**
		 * @param calibType the calibType to set
		 */
		public void setCalibType(String calibType) {
			this.calibType = calibType;
		}
		/**
		 * @return the calibFrequency
		 */
		public Integer getCalibFrequency() {
			return calibFrequency;
		}
		/**
		 * @param calibFrequency the calibFrequency to set
		 */
		public void setCalibFrequency(Integer calibFrequency) {
			this.calibFrequency = calibFrequency;
		}
		
}
